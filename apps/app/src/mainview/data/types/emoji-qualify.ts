/**
 * Set of Unicode codepoints that have an emoji variation sequence defined.
 * These codepoints need U+FE0F appended to render as colorful emoji in browsers
 * (they default to text/monochrome presentation without it).
 *
 * Source: Unicode 16.0 emoji-variation-sequences.txt (371 codepoints).
 */
export const TEXT_DEFAULT_EMOJI: Set<number> = new Set(EMOJI_VARIATION_BASES);

const VS16 = 0xfe0f;
const SKIN_TONE_START = 0x1f3fb;
const SKIN_TONE_END = 0x1f3ff;

/**
 * Add U+FE0F after text-default codepoints that don't already have it.
 * Leaves ZWJ sequences untouched (they're complex and their FE0F is intentional).
 */
export function qualifyEmoji(emoji: string): string {
  // ZWJ sequences are complex — leave them alone
  if (emoji.includes('\u200D')) return emoji;

  let result = '';
  let i = 0;

  while (i < emoji.length) {
    const cp = emoji.codePointAt(i) as number;
    const charLen = cp > 0xffff ? 2 : 1;
    result += emoji.slice(i, i + charLen);
    i += charLen;

    if (TEXT_DEFAULT_EMOJI.has(cp)) {
      // Check if already followed by FE0F
      const nextCp = i < emoji.length ? emoji.codePointAt(i) : undefined;
      if (nextCp === VS16) {
        // Already qualified — consume it
        result += '\uFE0F';
        i++;
      } else if (nextCp !== undefined && nextCp >= SKIN_TONE_START && nextCp <= SKIN_TONE_END) {
        // Skin tone modifier follows — don't add FE0F
      } else {
        // Insert FE0F
        result += '\uFE0F';
      }
    }
  }

  return result;
}

/**
 * Remove all U+FE0F variation selectors, UNLESS the string contains ZWJ (U+200D).
 * ZWJ sequences need their FE0F preserved to render correctly (e.g. rainbow flag).
 *
 * Matches telegram-tt's `removeVS16s()` approach.
 */
export function stripVS16(emoji: string): string {
  if (emoji.includes('\u200D')) return emoji;
  return emoji.replace(/\uFE0F/g, '');
}
